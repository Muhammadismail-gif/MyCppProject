#include<iostream>
using namespace std;
int main()
{
	cout<<"hello\a world"<<endl; // Alert or Alarm
	cout<<"hello\b world"<<endl; // Backspace
	cout<<"hello\f world"<<endl; // Form Feed
	cout<<"hello\n world"<<endl; // New Line
	cout<<"hello\r world"<<endl; // Carriage Return
	cout<<"hello\t world"<<endl; //Tab
	cout<<"hello\'world\'"<<endl; // Single Quote
	cout<<"hello\\world\\"<<endl; // Two Backslash
	return 0;
}